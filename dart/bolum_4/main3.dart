void main(){int s = 0;

do {
  print('do-while → $s');
  s++;
} while (s < 3);

print('Döngü bitti!');
// Bu kod, 0'dan 2'ye kadar olan sayıları yazdırır ve ardından "Döngü bitti!" mesajını gösterir.
} // Bu kod, do-while döngüsünü kullanarak 0'dan 2'ye kadar olan sayıları yazdırır ve ardından "Döngü bitti!" mesajını gösterir.
// Do-while döngüsü, koşul sağlanmasa bile en az bir kez çalışır.