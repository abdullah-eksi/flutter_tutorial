void main() {
  int a = 5;
  a += 2; // a = a + 2 → 7
  print(a);
  a *= 3; // a = a * 3 → 21
  print(a);
  a -= 4; // a = a - 4 → 17
  print(a);
  a ~/= 2; // a = a ~/ 2 → 8
  print(a);
  a %= 3; // a = a % 3 → 2
  print(a);
  a++; // a = a + 1 → 3
  print(a);
  a--; // a = a - 1 → 2
  print(a);
  a = 10; // a'yı tekrar 10 yapalım
  print(a);
}
